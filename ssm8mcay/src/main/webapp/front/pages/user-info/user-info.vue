<script>
	export default {
		data() {
			return {
				ruleForm: {
				},
				tableName:"",
				yonghuxingbieOptions: [],
				yonghuxingbieIndex: 0,
				yonghuxueliOptions: [],
				yonghuxueliIndex: 0,
				qiyeqiyexingzhiOptions: [],
				qiyeqiyexingzhiIndex: 0,
			}
		},
		computed: {
			baseUrl() {
				return this.$base.url;
			}
		},
		async onLoad() {
			let table = uni.getStorageSync("nowTable");
			let res = await this.$api.session(table);
			this.ruleForm = res.data;
			this.tableName = table;
			// 自定义下拉框值
			if(this.tableName=='yonghu'){
				this.yonghuxingbieOptions = "男,女".split(',');
				this.yonghuxingbieOptions.forEach((item, index) => {
					if(item==this.ruleForm.xingbie) {
						this.yonghuxingbieIndex = index;
					}
				});
			}
			// 自定义下拉框值
			if(this.tableName=='yonghu'){
				this.yonghuxueliOptions = "大专,本科,研究生".split(',');
				this.yonghuxueliOptions.forEach((item, index) => {
					if(item==this.ruleForm.xueli) {
						this.yonghuxueliIndex = index;
					}
				});
			}
			// 自定义下拉框值
			if(this.tableName=='qiye'){
				this.qiyeqiyexingzhiOptions = "外资,中外合资,国有企业,私人企业".split(',');
				this.qiyeqiyexingzhiOptions.forEach((item, index) => {
					if(item==this.ruleForm.qiyexingzhi) {
						this.qiyeqiyexingzhiIndex = index;
					}
				});
			}
			this.styleChange()
		},
		methods: {
                        // 下拉变化
                        yonghuxingbieChange(e) {
                                this.yonghuxingbieIndex = e.target.value
                                this.ruleForm.xingbie = this.yonghuxingbieOptions[this.yonghuxingbieIndex]
                        },
                        // 下拉变化
                        yonghuxueliChange(e) {
                                this.yonghuxueliIndex = e.target.value
                                this.ruleForm.xueli = this.yonghuxueliOptions[this.yonghuxueliIndex]
                        },
                        // 下拉变化
                        qiyeqiyexingzhiChange(e) {
                                this.qiyeqiyexingzhiIndex = e.target.value
                                this.ruleForm.qiyexingzhi = this.qiyeqiyexingzhiOptions[this.qiyeqiyexingzhiIndex]
                        },
			styleChange() {
				this.$nextTick(()=>{
					// document.querySelectorAll('.cu-form-group .uni-input-input').forEach(el=>{
					//   el.style.backgroundColor = this.userInfoForm.list.input.backgroundColor
					// })
				})
			},
			// 获取uuid
			getUUID () {
				return new Date().getTime();
			},
			logout() {
				uni.setStorageSync('token', '');
				this.$utils.jump('../login/login');
			},
			// 注册
			async update() {
				if(`yonghu` == this.tableName && this.ruleForm.yonghuzhanghao&&(!this.$validate.isMobile(this.ruleForm.yonghuzhanghao))){
					this.$utils.msg(`用户账号应输入手机格式`);
					return
				}
				if(`yonghu` == this.tableName && this.ruleForm.lianxifangshi&&(!this.$validate.isMobile(this.ruleForm.lianxifangshi))){
					this.$utils.msg(`联系方式应输入手机格式`);
					return
				}
				if(`yonghu` == this.tableName && this.ruleForm.youxiang&&(!this.$validate.isEmail(this.ruleForm.youxiang))){
					this.$utils.msg(`邮箱应输入邮件格式`);
					return
				}
				if(`yonghu` == this.tableName && this.ruleForm.clicknum&&(!this.$validate.isIntNumer(this.ruleForm.clicknum))){
					this.$utils.msg(`点击次数应输入整数`);
					return
				}
				if((!this.ruleForm.qiyemingcheng) && `qiye` == this.tableName){
					this.$utils.msg(`企业名称不能为空`);
					return
				}
				if((!this.ruleForm.mima) && `qiye` == this.tableName){
					this.$utils.msg(`密码不能为空`);
					return
				}
				if(`qiye` == this.tableName && this.ruleForm.gongsidianhua&&(!this.$validate.isPhone(this.ruleForm.gongsidianhua))){
					this.$utils.msg(`公司电话应输入电话格式`);
					return
				}
				if(`qiye` == this.tableName && this.ruleForm.lianxifangshi&&(!this.$validate.isMobile(this.ruleForm.lianxifangshi))){
					this.$utils.msg(`联系方式应输入手机格式`);
					return
				}
				if(`qiye` == this.tableName && this.ruleForm.qiyeyouxiang&&(!this.$validate.isEmail(this.ruleForm.qiyeyouxiang))){
					this.$utils.msg(`企业邮箱应输入邮件格式`);
					return
				}
				let table = uni.getStorageSync("nowTable");
				await this.$api.update(table, this.ruleForm);
				this.$utils.msgBack('修改成功');;
			}

			,yonghuzhaopianTap() {
				let _this = this;
				this.$api.upload(function(res) {
					_this.ruleForm.zhaopian = 'upload/' + res.file;
					_this.$forceUpdate();
				});
			}
		}
	}
</script>

